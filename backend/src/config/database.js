import pg from 'pg';
import dotenv from 'dotenv';

dotenv.config();

const { Pool } = pg;

// PostgreSQL connection pool
const pool = new Pool({
  host: process.env.DB_HOST || 'localhost',
  port: parseInt(process.env.DB_PORT || '5432'),
  database: process.env.DB_NAME || 'monitor_db',
  user: process.env.DB_USER || 'monitor_user',
  password: process.env.DB_PASSWORD || 'monitor_password',
  min: parseInt(process.env.DB_POOL_MIN || '2'),
  max: parseInt(process.env.DB_POOL_MAX || '10'),
  idleTimeoutMillis: 30000,
  connectionTimeoutMillis: 10000, // Increased to 10 seconds for first connection
});

// Test database connection
pool.on('connect', () => {
  console.log('âœ“ Database connected successfully');
});

pool.on('error', (err) => {
  console.error('Unexpected database error:', err);
  // Don't exit on error - let the pool handle reconnection
});

// Helper function to execute queries
export const query = async (text, params) => {
  const start = Date.now();
  try {
    const res = await pool.query(text, params);
    const duration = Date.now() - start;
    console.log('Executed query', { text, duration, rows: res.rowCount });
    return res;
  } catch (error) {
    console.error('Database query error:', error);
    throw error;
  }
};

// Helper function to get a client from pool for transactions
export const getClient = async () => {
  return await pool.connect();
};

export default pool;
